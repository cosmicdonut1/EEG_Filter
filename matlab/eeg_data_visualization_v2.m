function data_visualization = eeg_data_visualization2(data, step, min_value, max_value, R, sigma, apply_blur)
    % data - массив имен электродов и входных значений сигнала на
    % электроде. Вид: {{'name' val}}
    % step - дробный шаг интерполяции
    % min_value - минимальное значение для нормализации
    % max_value - максимальное значение для нормализации
    % R - координата ограничивающего квадрата
    % sigma - стандартное отклонение для гауссова размытия
    % apply_blur - флаг, указывающий, нужно ли применить размытие (true/false)

    % Получаем информаци о положении всех электродов и их число
    info_points = get_info_points();
    num_all_points = size(info_points, 1);
    % Получаем число электродов
    num_in_points = size(data, 1);
    % Инициализируем массивы
	name = cell(num_in_points,1);
    val = zeros(num_in_points, 1);
    x = zeros(num_in_points, 1);
    y = zeros(num_in_points, 1);

    for i = 1:num_in_points
        % Получаем входное имя электрода
        curr_name = data{i}(1, 1);
        % Получаем сферические координаты электрода по его имени
        for j = 1:num_all_points
            point_name = info_points{j}(1, 1);
            if (strcmp(point_name, curr_name))
                % Получаем входное имя электрода
                name(i) = data{i}(1, 1);
                % Получаем входное значение сигнала на электроде
                val(i) = cell2mat(data{i}(1, 2));
                % Получаем сферические координаты точки
                r =  cell2mat(info_points{j}(1, 2));
                theta = cell2mat(info_points{j}(1, 3));
                phi = cell2mat(info_points{j}(1, 4));
                % Преобразуем углы из градусов в радианы
                theta = deg2rad(theta);
                phi = deg2rad(phi);
                % Вычисляем декартовы координаты
                x_point = r * sin(theta) * cos(phi);
                y_point = r * sin(theta) * sin(phi);
                z_point = r * cos(theta);
                % Вычисляем стереографическую проекцию
                mu = 1.0 / (1.0 + z_point);
                x(i) = x_point * mu;
                y(i) = y_point * mu;
                break;
            end
        end
    end

    % Создаем массив углов контура от 0 до 360 градусов с шагом 0.01
    contour_angles = 0:0.01:360;
    % Переводим углы в радианы
    contour_angles_rad = deg2rad(contour_angles);
    
    % Вычисляем координаты точек контура на окружности
    contour_x_coords = cos(contour_angles_rad);
    contour_y_coords = sin(contour_angles_rad);
    
    % Создаем массив значений для каждой точки контура
    contour_values = zeros(1,size(contour_angles, 2)); 
    %
    contour_x_coords = contour_x_coords(:);
    contour_y_coords = contour_y_coords(:);
    contour_values = contour_values(:);
    % 
    x = vertcat(x,contour_x_coords);
    y = vertcat(y,contour_y_coords);
    val = vertcat(val,contour_values);

    % Определение ограничивающего прямоугольника
    min_x = -R;
    max_x = R;
    min_y = -R;
    max_y = R;
    % Создание сетки координат с определенным шагом
    [X, Y] = meshgrid(min_x:step:max_x, min_y:step:max_y);
    % Интерполяция значений по сетке
    interpolated_values = griddata(x, y, val, X, Y);
    % Замена значений NaN на 0
    interpolated_values(isnan(interpolated_values)) = 0;
    % Нормализация значений
    interpolated_values = (interpolated_values - min_value) / (max_value - min_value);
    % Применение гауссова размытия
    if (apply_blur)
        interpolated_values = imgaussfilt(interpolated_values, sigma);
    end
    % Получаем данные визуализации
    data_visualization = interpolated_values;

    % Вывод на график для отладки
	
    debug = true;
    if (debug)
	    % Создание графика
	    figure;
	    hold on;
        % Вывод 2D-карты с билинейной интерполяцией
        colormap(hot);
        imagesc(X(1, :), Y(:, 1), data_visualization);
	    % Вывод точек
	    for i = 1:num_in_points
	        % Добавление подписей к точкам
	        plot(x(i), y(i), 'ro', 'MarkerSize', 3, 'MarkerFaceColor', 'r');
            %scatter(x(i), y(i));
		    text(x(i), y(i), name{i}, 'VerticalAlignment', 'top', 'HorizontalAlignment', 'center');
        end
	    % Настройка вида графика
        axis off
	    xlabel('X');
	    ylabel('Y');
        caxis([0, 1]);
        colorbar;
        title('Карта визуализации данных ЭЭГ');
	    hold off;
    end
    
    % Вывод на график для отладки
    debug = false;
    if (debug)
        % creating the countour plot
        val = val(:);
        x = x(:);
        y = y(:);
        name = name(:);
        [x_, y_] = meshgrid((0:1:360) * pi / 180, 0:0.01:1);
        [X_, Y_] = pol2cart(x_, y_);
        F = scatteredInterpolant(x, y, val, 'natural', 'linear');
        Z_ = F(X_, Y_);
        figure
        contourf(X_, Y_, Z_, 1000, 'LineColor', 'none');
        title('Карта визуализации данных ЭЭГ');
        daspect([1 1 1])
        colormap(hot);
        set(gca,'XTick', [],  'YTick', [])
        hold on
	    % Вывод точек
	    for i = 1:num_in_points
	        % Добавление подписей к точкам
	        plot(x(i), y(i), 'ro', 'MarkerSize', 5, 'MarkerFaceColor', 'r');
            scatter(x(i), y(i));
		    text(x(i), y(i), name{i}, 'VerticalAlignment', 'top', 'HorizontalAlignment', 'center');
        end    

        caxis([0, 1]);
        colorbar;
        hold off;
    end
end

